//############
// Math in JS
//############
let num1 = 100;
console.log(num1 + 25);
console.log(num1 - 100);
console.log(num1 * 100);
console.log(num1 / 1500);
console.log(num1 % 1500); // Reminder
console.log(num1++); // returns the value of i before incrementing.
console.log(++num1); // returns the value of i after it has been incremented.
console.log(num1--); // returns the value of i before decrementing.
console.log(--num1); // returns the value of i after it has been decremented.
console.log(Math.PI); //  PI
console.log(Math.sqrt(4)); // 4 sqrt ? = 2
console.log(Math.pow(2, 2)); // 2 pow 2 ? = 4